namespace Imgur.API.Models
{
    /// <summary>
    ///     Indicates the order that a list of items are sorted.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        ///     Sort the list by the newest item first.
        /// </summary>
        Newest,

        /// <summary>
        ///     Sort the list by the oldest item first.
        /// </summary>
        Oldest
    }
}